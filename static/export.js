function roundVector(vector, digits = 6) {
  const factor = 10 ** digits;
  return vector.map((value) => Math.round(value * factor) / factor);
}

function downloadJson(filename, payload) {
  const blob = new Blob([JSON.stringify(payload, null, 2)], {
    type: "application/json",
  });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

export function exportArmDefinition(state) {
  const payload = {
    name: "custom_model",
    description: `Generated by SocioMol Arm Builder from ${state.map.fileName || "unknown map"}`,
    schema_version: "0.2.0",
    coordinate_system: {
      origin: "map_box_center",
      units: "angstrom",
      axis_order: ["x", "y", "z"],
    },
    source: {
      map_file: state.map.fileName,
      model_file: state.model.fileName,
      grid_size_voxels: state.map.metadata?.grid_size_voxels ?? null,
      voxel_size_angstrom: state.map.metadata?.voxel_size_angstrom ?? null,
      box_size_angstrom: state.map.metadata?.box_size_angstrom ?? null,
    },
    arms: state.arms.map((arm) => ({
      anchor: roundVector(arm.point1_xyz),
      direction_point: roundVector(arm.point2_xyz),
      tangent: arm.tangent || "direction_point_to_anchor",
    })),
  };

  downloadJson("arm_geometry.json", payload);
}

export function exportPreviewMarkers(state) {
  const markers = [];

  state.arms.forEach((arm) => {
    markers.push({
      id: `${arm.id}_point1`,
      label: `${arm.name} point 1`,
      position_xyz: roundVector(arm.point1_xyz),
      kind: "point1",
    });
    markers.push({
      id: `${arm.id}_point2`,
      label: `${arm.name} point 2`,
      position_xyz: roundVector(arm.point2_xyz),
      kind: "point2",
    });
  });

  downloadJson("arm_preview_markers.json", {
    schema_version: "0.1.0",
    coordinate_system: {
      origin: "map_box_center",
      units: "angstrom",
      axis_order: ["x", "y", "z"],
    },
    markers,
  });
}
